using UnityEngine;
using UnityEngine.UI;
using System.Collections;

public class CZParamCell : MonoBehaviour {
//===========================================================
// 変数宣言
//===========================================================
	//---------------------------------------------------
	// public
	//---------------------------------------------------
	public string	Param		{ get { return m_TxtParam.text; } }
	public float	CellW		{ get { return m_vCellSize.x; } }
	public float	CellH		{ get { return m_vCellSize.y; } }

	//---------------------------------------------------
	// private
	//---------------------------------------------------
	private Text	m_TxtParam;
	private Vector2	m_vCellSize;

//===========================================================
// 関数定義
//===========================================================
	//---------------------------------------------------
	// コンストラクタ
	//---------------------------------------------------
	void Awake()
	{
		m_TxtParam = transform.FindChild("Text").GetComponent<Text>();
		m_vCellSize	= GetComponent<RectTransform>().sizeDelta;
	}
	//---------------------------------------------------
	// 最初の更新
	//---------------------------------------------------
	void Start()
	{
	
	}
	//---------------------------------------------------
	// 更新処理
	//---------------------------------------------------
	void Update()
	{
	
	}
	//---------------------------------------------------
//===========================================================
}
